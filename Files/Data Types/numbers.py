# 1. NUMBERS
#     - integers (1, 2, 3)
#     - float (up to 15 decimal numbers)
#     - complex numbers\

int_variable1 = 2
int_variable2 = 10
float_variable = 1.23

print("This is addition =", int_variable1 + int_variable2)  # addition
print("This is subtraction =", int_variable1 - int_variable2)  # subtraction
print("This is multiplication =", int_variable1 * int_variable2)  # multiplication
print("This is division =", int_variable1 / int_variable2)  # division

print("This is datatype for int_variable1 =", type(int_variable1))  # know the datatype

print("Converting int to float =", float(int_variable1))  # convert int to float
print("Converting float to int =", int(float_variable))  # convert float to int