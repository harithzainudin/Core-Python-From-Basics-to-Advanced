# if-elif-elif...else
color = input("Enter any color: ")
if color == "red":
    print("You entered RED")
elif color == "green":
    print("You entered GREEN")
elif color == "blue":
    print("You entered BLUE")
elif color == "yellow":
    print("You entered YELLOW")
elif color == "black":
    print("You entered BLACK")
else:
    print("Uknown color")


